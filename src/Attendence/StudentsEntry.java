    public void save() {
        try {
            ID = txtStudId.getText();
            NAME = txtStudName.getText().trim();
            USERNAME=txtUserName.getText().trim();
            PASSWORD=txtPassWord.getText().trim();
            STD = cmbStd.getText();
            PHNO = txtParentPhoneNo.getText();
            EMAIL = txtParentemail.getText();
            
            if (chkExistsid(ID)) {
               JOptionPane.showMessageDialog(this, "Student Id already in exists", "ERROR", JOptionPane.ERROR_MESSAGE);
                txtStudName.grabFocus();
                return; 
            }

            String studentStd = cmbStd.getText();
            String student_name = txtStudName.getText().trim();
            String password = txtPassWord.getText().trim();
            if (student_name.length() < 4 || student_name.equals("")) {
                JOptionPane.showMessageDialog(this, "Enter Proper Student Name !!", "ERROR", JOptionPane.ERROR_MESSAGE);
                txtStudName.grabFocus();
                return;
            }

            String phoneNumber = txtParentPhoneNo.getText();
            if (phoneNumber.trim().equals("")) {
                JOptionPane.showMessageDialog(this, "Enter Phone Number !!!", "ERROR", JOptionPane.ERROR_MESSAGE);
                txtParentPhoneNo.grabFocus();
                return;
            }

            String email = txtParentemail.getText();
            if (!isEmailValid(email)) {
                JOptionPane.showMessageDialog(this, "Enter proper EmailId", "Error", JOptionPane.ERROR_MESSAGE);
                txtParentemail.grabFocus();
                return;
            }

            if (!isPhoneNumberValid(phoneNumber)) {
                JOptionPane.showMessageDialog(this, "Enter Vaild Phone Number", "ERROR", JOptionPane.ERROR_MESSAGE);
                txtParentPhoneNo.grabFocus();
                return;
            }

            if (addflag) {
                if (!chkExists(student_name, studentStd)) {
                    this.dispose();
                    Thread t = new Thread() {
                        public void run() {
                            try {
                                Thread.sleep(1000);
                                new FaceRecognizer1().recognizeFromCam1();
                            } catch (Exception ae) {
                                JOptionPane.showMessageDialog(null, ae);
                            }
                        }
                    };
                    
                    t.start();
                    
                } else {
                    JOptionPane.showMessageDialog(this, "Already Exists Entry!!!!");
                }
            }
            
            if (modifyflag) {
                String query = "UPDATE STUDENT SET "
                        + " student_name='" + student_name + "',"
                        + " student_std='" + studentStd + "',"
                        + " password='" +  password + "',"
                        + " phoneno='" + phoneNumber + "'"
                        + " WHERE sid='" + ID + "'";

                int ans = dbcon.getUpdate(query);
                if (ans == 1) {
                    JOptionPane.showMessageDialog(this, "Sucssefully Update Student Data!!!");
                } else {
                    JOptionPane.showMessageDialog(this, "Failed To Update Data!!!");
                }
            }

            txtStudId.setText("");
            txtStudName.setText("");
            txtParentPhoneNo.setText("");
            txtParentemail.setText("");
            btnAdd.setEnabled(true);
            btnUpdate.setEnabled(true);
            btnSave.setEnabled(false);
            loadListData("SELECT * from student");
        } catch (NumberFormatException | HeadlessException e) {
        }

    }

