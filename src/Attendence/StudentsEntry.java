    public void loadListData(String q) {
        try {
            Vector data = new Vector();
            String Query = q;
            //System.err.println(q);
            ResultSet rs = dbcon.getResultSet(Query);
            if (rs.next()) {
                do {
                    data.add(rs.getString("Student_Name"));
                } while (rs.next());
            }
            rs.close();
            lstStudentNames.removeAll();
            lstStudentNames.setListData(data);
        } catch (Exception e) {
        }
    }

