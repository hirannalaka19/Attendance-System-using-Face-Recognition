    public int getStudId() {
        int id = 1;
        try {
            String query = "SELECT MAX(sid) FROM student";
            ResultSet rs = dbcon.getResultSet(query);
            if (rs.next()) {
                String retId = rs.getString("sid");
                if (retId != null) {
                    id = Integer.parseInt(retId.trim()) + 1;
                }
            }
        } catch (SQLException | NumberFormatException e) {
        }
        return id;
    }

